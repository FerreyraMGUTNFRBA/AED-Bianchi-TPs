#include <iostream>

using namespace std;

int main(){

    int a, b, suma, dif, prod;

    cout << "Ingrese un numero entero: " << endl;
    cin >> a;
    cout << "Ingrese un numero entero: " << endl;
    cin >> b;

    suma = a + b;

    dif = a - b;

    prod = a * b;

    cout << "La suma de ambos es: " << suma << endl;

    cout << "La diferencia entre ambos es: " << dif << endl;

    cout << "El producto de ambos es: " << prod << endl;

}
